package Kotlin

fun main() {
    //with type inference, kotlin can deduce the type based on the value assigned when defining the variable for example
    //this is how you define a variable that will store an integer
    var n = 10
    //this is how you define a variable that will store a long
    var l = 100L
    //this is how you define a variable that will store a double
    var d = 3.14
    //this is how you define a variable that will store a float
    var f = 3.14F
    //this is how you define a variable that will store a boolean
    var b = false
    //this is how you define a variable that will store a string
    var s = "David"
    //this is how you define a variable that will store a character
    var c = 'A'
}