package Kotlin

fun main() {
    /**
     * We use comparison operators to compare values and return a boolean as the result
     * some comparison operators include
     * < >  ==  >=  <=
     */

    //examples
    val n1 = 10
    val n2 = 4

    println(n1 > n2)
    println(n1 >= n2)
    println(n1 < n2)
    println(n1 <= n2)
    println(n1 == n2)

}